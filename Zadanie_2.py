print(int(3.0))  #Konwersja liczby zmiennoprzecinkowej na liczbę całkowitą. Wynik 3
print(float(3))  #Konwersja liczby całkowitej na liczbę zmiennoprzecinkową. Wynik 3.0
print(float("3"))  #Konwersja liczby zapisanej jako string na liczbę zmiennoprzecinkową. Wynik 3.0
print(str(12.4))  #Konwersja liczby zmiennoprzecinkowej na ciąg znaków. Wynik '12.4'
print(bool(0))  #Konwersja liczby całkowitej na wartość logiczną. Wynik False
